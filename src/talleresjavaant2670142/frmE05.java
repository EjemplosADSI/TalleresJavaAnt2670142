    public boolean isNumber(String numero) {
        try {
            int Number = Integer.parseInt(numero);
            return true;
        } catch (NumberFormatException NFE) {
            JOptionPane.showMessageDialog(this,
                    "El texto " + numero + " no es un numero valido",
                    "Numero Invalido", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String textNumber = jtxtFecha.getText();
        if (textNumber.length() == 8) {
            if (isNumber(textNumber)) {
                int Number = Integer.parseInt(textNumber);
                if (Number > 0) {
                    int Anio = Integer.parseInt(textNumber.substring(0, 4));
                    int Mes = Integer.parseInt(textNumber.substring(4, 6));
                    int Dia = Integer.parseInt(textNumber.substring(6, 8));
                    if (Mes <= 0 || Mes > 12){
                        JOptionPane.showMessageDialog(this,
                            "Mes incorrecto: "+Mes+". Debe ser mayor a 0 y menor a 12",
                            "Numero Invalido", JOptionPane.ERROR_MESSAGE);
                    }
                    else if (Dia <= 0 || Dia > 31){
                        JOptionPane.showMessageDialog(this,
                            "Dia incorrecto: "+Dia+". Debe ser mayor a 0 y menor a 31",
                            "Numero Invalido", JOptionPane.ERROR_MESSAGE);
                    }else{
                        lblResultado.setText("<html><b>AÃ±o: </b>"+Anio+"<br><b>Mes: </b>"+Mes+"<br><b>Dia: </b>"+Dia+"</html>");
                    }
                } else {
                    JOptionPane.showMessageDialog(this,
                            "El numero " + jtxtFecha.getText() + " debe ser positivo",
                            "Numero Invalido", JOptionPane.ERROR_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this,
                "Formato incorrecto " + jtxtFecha.getText() + " la longitud debe ser 8",
                "Numero Invalido", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

